    public static class LoginUtils {

        /**
         * 判断是否是邮箱
         *
         * @param email 指定的字符串
         * @return 是否是邮箱:是为true，否则false
         */
        public static Boolean isEmail(String email) {
            Boolean isEmail = false;
            String expr = "^([a-zA-Z0-9_\\-\\.]+)@((\\[[0-9]{1,3}\\.[0-9]{1,3}\\.[0-9]{1,3}\\.)|(([a-zA-Z0-9\\-]+\\.)+))([a-zA-Z]{2,4}|[0-9]{1,3})$";

            if (email.matches(expr)) {
                isEmail = true;
            }
            return isEmail;
        }

        /**
         * 判断是否是手机号
         *
         * @param phone
         * @return
         */
        public static boolean isPhone(String phone) {
            Pattern pattern = Pattern
                    .compile("^(13[0-9]|15[0-9]|18[0-9]|19[0-9]|14[5-9]|16[124567]|17[0-8])\\d{8}$");
            Matcher matcher = pattern.matcher(phone);

            if (matcher.matches()) {
                return true;
            }
            return false;
        }

        /**
         * 手机号用****号隐藏中间数字
         *
         * @param phone
         * @return
         */
        public static String setPhoneStar(String phone) {
            String phone_s = phone.replaceAll("(\\d{3})\\d{4}(\\d{4})", "$1****$2");
            return phone_s;
        }

        /**
         * 邮箱用****号隐藏前面的字母
         *
         * @return
         */
        public static String setEmailStar(String email) {
            String emails = email.replaceAll("(\\w?)(\\w+)(\\w)(@\\w+\\.[a-z]+(\\.[a-z]+)?)", "$1****$3$4");
            return emails;
        }

        /**
         * 设置特殊字符过滤器
         *
         * @param editText
         */
        public static void setSpecialFilter(EditText editText) {
            String editable = editText.getText().toString();
            String regEx = "[^a-zA-Z0-9!@#$%^._-]";  //只能输入字母或数字
            Pattern p = Pattern.compile(regEx);
            Matcher m = p.matcher(editable);
            String str = m.replaceAll("").trim();    //删掉不是字母或数字的字符
            if(!editable.equals(str)){
                editText.setText(str);  //设置EditText的字符
                editText.setSelection(str.length()); //因为删除了字符，要重写设置新的光标所在位置
            }
        }
    }
